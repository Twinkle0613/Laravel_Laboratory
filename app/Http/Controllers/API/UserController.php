     * @return \Illuminate\Http\Response
     */
    public function store(StoreUserRequest $request)
    {
        $data = $request->all();
        $data['password'] = bcrypt($data['password']);
        return (new UserResource(User::create($data)))->response()->setStatusCode(Response::HTTP_CREATED);
    }

